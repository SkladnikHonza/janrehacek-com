let currentLang = 'cs';
const translations = window.translations;

// Cache PluralRules instances; one per language we've already seen.
const _pluralRules = {};
function pluralCategory(lang, n) {
    if (typeof Intl === 'undefined' || !Intl.PluralRules) {
        // No Intl.PluralRules → fall back to English-style one/other.
        return n === 1 ? 'one' : 'other';
    }
    if (!_pluralRules[lang]) {
        try { _pluralRules[lang] = new Intl.PluralRules(lang); }
        catch (e) { _pluralRules[lang] = new Intl.PluralRules('en'); }
    }
    return _pluralRules[lang].select(n);
}

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    const t = translations[lang];

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) el.textContent = t[key];
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (t[key]) el.innerHTML = t[key];
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) el.setAttribute('placeholder', t[key]);
    });

    // Plural-aware nodes: data-i18n-key="listings.count" + data-i18n-count="3"
    // → look up listings.count.{one|few|many|other}, interpolate {n}.
    document.querySelectorAll('[data-i18n-key][data-i18n-count]').forEach(el => {
        const baseKey = el.getAttribute('data-i18n-key');
        const n       = parseInt(el.getAttribute('data-i18n-count'), 10);
        if (!Number.isFinite(n)) return;
        const cat   = pluralCategory(lang, n);
        const tpl   = t[`${baseKey}.${cat}`] || t[`${baseKey}.other`];
        if (tpl == null) return;
        el.textContent = String(tpl).replace(/\{n\}/g, n);
    });

    const flagEl = document.getElementById('langCurrentFlag');
    const codeEl = document.getElementById('langCurrentCode');
    if (flagEl) flagEl.textContent = langInfo[lang].flag;
    if (codeEl) codeEl.textContent = langInfo[lang].code;

    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
    });

    document.documentElement.setAttribute('lang', lang);

    try { localStorage.setItem('preferredLang', lang); } catch(e) {}

    const sw = document.getElementById('langSwitcher');
    if (sw) sw.classList.remove('open');
}

function toggleLangMenu() {
    document.getElementById('langSwitcher').classList.toggle('open');
}

document.addEventListener('click', function(e) {
    const switcher = document.getElementById('langSwitcher');
    if (switcher && !switcher.contains(e.target)) {
        switcher.classList.remove('open');
    }
});

(function initLang() {
    let lang = 'cs';
    try {
        const saved = localStorage.getItem('preferredLang');
        if (saved && translations[saved]) {
            lang = saved;
        } else {
            const browserLang = (navigator.language || 'cs').toLowerCase().split('-')[0];
            if (translations[browserLang]) lang = browserLang;
        }
    } catch(e) {}
    setLanguage(lang);
})();


